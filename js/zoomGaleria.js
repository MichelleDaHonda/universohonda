const img1 = document.getElementById('img1');

function zoomIn1(){
    const img1 = document.getElementById('img1')
    img1.style.transform = 'scale(1.5)';
    
}

function zoomOut1(){
    const img1 = document.getElementById('img1')
    img1.style.transform = 'scale(1)';
   
}

img1.addEventListener('mouseover', zoomIn1);
img1.addEventListener('mouseout', zoomOut1);



